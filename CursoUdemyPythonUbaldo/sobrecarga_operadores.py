# Operadores binarios
#
# Operator            Magit Method
# +                   __add__(self, other)
# -                   __sub__(self, other)
# *                   __mul__(self, other)
# /                   __div__(self, other)
# //                  __floordiv__(self, other)
# %                   __mod__(self, other)
# **                  __pow__(self, other)
#
#     OPERADORES DE COMPARACION
#
#     Operator            Magic Method
#     <                   __it__(self, other)
#     >                   __gt__(self, other)
#     <=                  __le__(self, other)
#     >=                  __ge__(self, other)
#     ==                  __eq__(self, other)
#     !=                  __ne__(self, other)
#
# OPERADORES DE ASIGNACION
#
# Operator            Magic Method
# -=                  __isub__(self, other)
# +=                  __iadd__(self, other)
# *=                  __imul__(self, other)
# /=                  __idiv__(self, other)
# //                  __ifloordiv__(self, other)
# %=                  __imood__(self, other)
# **=                 __ipow__(self, other)
#
# OPERADORES UNARIOS
#
# Operator            Magic Method
# -                   __neg__(self, other)
# +                   __pos__(self, other)
# ~                   __invert__(self, other)


